            <div class="container">
                <h1 class="center section-heading-black">HISTORIA</h1>
                <div class="line-black"></div>
                <p class="center">
                    La historia de Shamrock Chassis inicia unos años atrás en Chihuahua, México, donde un hombre de familia y empresario, junto a sus cuatro hijos, tuvieron la idea de entrar al entorno de carreras locales. El jefe de la familia Pérez, Don Humberto y sus cuatro hijos, Chatato, Keko, Gus y Perico aterrizaron en el mundo del Dirt Racing.
                </p>
                <p class="center">
                    Empezaron como cualquiera, yendo a las pistas, preguntando como podrían ser parte de ello, ¿Cómo podrían obtener sus primeros carros? ¿Cómo podrían ganar sus primeras carreras? Las preguntas habituales para alguien nuevo en cualquier tipo de actividad.
                </p>
                <p class="center">
                    No fue fácil, pero se mantuvieron enfocados en su objetivo, pelearon y trabajaron duro para lograrlo.
                </p>
                <p class="center">
                    Primero se convirtieron en conductores, cuando cumplieron 18 años fueron obteniendo sus carros. Empezaron a correr con carros preparados por viejos mecánicos, obteniendo buenos, regulares y malos resultados. Después de ello, entendieron que si quieres que algo sea bien hecho, lo tienes que hacer tu mismo, por lo que empezaron a construir y preparar sus propios carros. Llegaron victorias, al igual que campeonatos y finalmente buena fama alrededor del estado de Chihuahua. ¡El Shamrock Racing había nacido!
                </p>
                <p class="center">
                    Pero ganar carreras no era la única cosa que tenían en mente, entonces empezaron a construir y preparar carros para otros competidores y así fue como crearon un nuevo negocio llamado Shamrock Chassis. Con esta nueva adición a su línea de negocio, fueron conocidos como "los principales partidarios del deporte del Dirt Racing en el estado de Chihuahua", que era en ese entonces el único estado en México donde se practicaba este deporte.
                </p>
                <p class="center">
                    Desde el inicio de 2015, Keko Pérez el segundo hermano mayor, empezó a competir en competencias locales en El Paso, TX y Las Cruces, NM de últimos modelos, obteniendo buenos resultados terminando en segundo lugar en eventos principales y varios primeros lugares en series de eliminatorias. Terminó obteniendo el quinto lugar en el campeonato.
                </p>
                <p class="center">
                    También, dentro de este año 2015, después de múltiples problemas con organizadores y autoridades locales en Chihuahua, el tercer hermano mayor, Gustavo Pérez, conocido como "Gus", se comprometió de gran manera con su ciudad natal y decidió reconstruir una vieja pista abandonada para convertirla en una nueva instalación de Dirt Racing empleando todos los estándares de seguridad como cualquier pista de carreras oval profesional en estados Unidos, el ahora famoso Autódromo Aquiles. Este año con dos carreras, pero para 2016 con 10 carreras en el calendario. También se encuentran involucrados en promover y patrocinar carreras en el circuito de asfalto en una instalación local llamada Autódromo Internacional Francisco Villa, con al menos 8 carreras para el campeonato del 2016.
                </p>
                <p class="center">
                    Con este último paso, la familia Pérez se ha convertido en promotores de este su querido deporte. Pero eso no es todo, ahora, el hermano mayor Humberto Pérez, conocido como Chatato, quiere integrar su negocio de una manera más grande. Quiere convertirse en distribuidor de equipo de carreras como auto partes, equipo de seguridad, llantas, cascos, trajes de conducción, ruedas, etc. Como se puede observar, esta familia lleva gasolina, aceite y carreras por sus venas, y ahora se pueden reconocer como los partidarios más grandes del Dirt y Circuit Racing del estado de Chihuahua.
                </p>
                <p class="center">
                    Lo siguiente es una pequeña muestra de lo que han hecho, lo que hacen y lo que harán por el motorsport en Chihuahua, como su lema dice:
                </p>
                <p class="center">
                    "Shamrock, construimos tus sueños de carreras"
                </p>
